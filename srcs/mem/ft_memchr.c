#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int b;

	b = 0;
	while (b < (int)n)
	{
		if (((char *)s)[b] == ((char)c))
			return ((void *)s + b);
		b++;
	}
	return (NULL);
}
