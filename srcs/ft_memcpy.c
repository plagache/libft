#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	int b;

	b = 0;
	while (b < (int)n)
	{
		((char *)dst)[b] = ((char *)src)[b];
		b++;
	}
	return (dst);
}
