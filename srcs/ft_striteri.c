#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int c;

	c = 0;
	if (!(s == 0) && f != 0)
	{
		while (s[c])
		{
			f(c, (s + c));
			c++;
		}
	}
}
