#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	unsigned int	c;

	c = 0;
	while (c < len && src[c])
	{
		dst[c] = src[c];
		c++;
	}
	while (c < len)
	{
		dst[c] = '\0';
		c++;
	}
	return (dst);
}
