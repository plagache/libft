#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int c;
	int d;

	d = 0;
	c = 0;
	while (s1[c])
		c++;
	while (s2[d])
	{
		s1[c] = s2[d];
		c++;
		d++;
	}
	s1[c] = '\0';
	return (s1);
}
