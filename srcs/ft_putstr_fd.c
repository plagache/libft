#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int c;

	c = 0;
	if (!(s == 0))
	{
		while (s[c])
		{
			ft_putchar_fd(s[c], fd);
			c++;
		}
	}
}
